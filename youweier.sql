SET NAMES UTF8;
DROP DATABASE IF EXISTS ywe;
CREATE DATABASE ywe CHARSET=UTF8;
USE ywe;
/**小吃标签**/
CREATE TABLE catalog(
  cid INT PRIMARY KEY AUTO_INCREMENT,
  cname VARCHAR(32)
);

/**小吃详细信息**/
CREATE TABLE food(
  fid INT PRIMARY KEY AUTO_INCREMENT,
  catalog_id INT,              #所属标签编号
  title VARCHAR(128),         #主标题
  subtitle VARCHAR(128),      #副标题
  ftime VARCHAR(64),        #烹煮时间
  type VARCHAR(64),           #口味
  how varchar(64),           #怎么做
  main_stuff VARCHAR(64),           #主材料
  other_stuff VARCHAR(64),           #辅材料
  image VARCHAR(64),           #图片
  skill varchar(128),
FOREIGN KEY(catalog_id) REFERENCES catalog(cid)

);
create Table detail (
  did INT PRIMARY KEY AUTO_INCREMENT,
  food_id INT,
  process varchar(128),
  image varchar(128),
  FOREIGN KEY(food_id) REFERENCES food(fid)
);
